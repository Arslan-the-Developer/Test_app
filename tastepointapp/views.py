def products(request):
    if request.method=="GET":
        search_query = request.GET.get('search_word')
        if search_query is not None:
            prods = Product.objects.filter(Q(prodcut_category__iexact=search_query) | Q(product_name__icontains=search_query))
        else:
            prods = Product.objects.all().order_by('?')
    else:
        prods = Product.objects.all().order_by('?')
    return render(request,'products.html',{'prods':prods})


def product_detail(request, pk):
    product = get_object_or_404(Product, pk=pk)
    if request.method == "POST":
        quantity = 0
        try:
            quantity = int(request.POST.get('prod_quantity'))
        except ValueError:
            pass
        if quantity > 0:
            cart, created = Cart.objects.get_or_create(user=request.user)
            cart_product, created = CartProduct.objects.get_or_create(cart=cart, product=product, defaults={'product_quantity': quantity})
            if not created:
                cart_product.product_quantity = quantity
                cart_product.save()
    prods = Product.objects.filter(Q(prodcut_category__iexact=product.prodcut_category) | Q(product_name__icontains=product.product_name) | Q(product_description__icontains=product.product_name)).exclude(product_name=product.product_name)
    return render(request, 'product_detail.html', {'product': product, 'prods': prods})


def user_cart(request):
    cart, created = Cart.objects.get_or_create(user=request.user)
    cart_products = CartProduct.objects.filter(cart=cart)
    total_cart_products = sum(([product.product_quantity for product in cart_products]))
    total_price = sum([product.product.product_price * product.product_quantity for product in cart_products])
    if request.method == "POST":
        name_of_product_to_delete = request.POST.get('del_prod')
        del_product = Product.objects.get(product_name=name_of_product_to_delete)
        product_to_delete = CartProduct.objects.filter(product=del_product)
        product_to_delete.delete()
        return redirect("/cart")
    return render(request, 'cart.html', {'cart_products': cart_products,'total_prods':total_cart_products,'total_price':total_price})

def clear_cart(request):
    CartProduct.objects.all().delete()
    return redirect("/cart")


