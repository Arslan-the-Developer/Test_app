def handle_payment(request):
    referer = request.META.get('HTTP_REFERER')
    cart = Cart.objects.get(user=request.user)
    cart_products = CartProduct.objects.filter(cart=cart)
    total_price = sum([product.product.product_price * product.product_quantity for product in cart_products])
    if request.method == "POST":
        try:
            card_number = request.POST.get('card_number')
            card_holder_name = request.POST.get('card_holder')
            expiration_month = request.POST.get('expiration_month')
            expiration_year = request.POST.get('expiration_year')
            cvc = request.POST.get('cvc')
            return redirect("/order_success")
        except Exception as identifier:
            return redirect("/payment")
    return render(request,"payment.html",{'amount_to_pay':total_price,'referer':referer})


